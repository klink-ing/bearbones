#!/usr/bin/env node
/**
 * Inline `src/templates/*.ts` into a single generated module
 * `src/templates.generated.ts` consumed by `src/codegen-templates.ts`.
 *
 * Why this exists: Panda's config loader bundles consumers via esbuild and
 * may emit CJS, which strips `import.meta.url` to an empty string. That
 * breaks any runtime `fs.readFileSync(new URL("./templates/...", import.meta.url))`
 * scheme. By inlining the template bodies as string constants at build /
 * test setup time, the loader has no I/O and no env-quirks at runtime —
 * the strings are baked into the bundle.
 *
 * Triggered automatically by:
 *   - vitest `globalSetup` (so `vp test` works on a fresh clone)
 *   - the `inlineTemplatesPlugin` tsdown plugin in `vite.config.ts` (so
 *     `vp pack` and `vp pack --watch` regenerate before each build, and
 *     watch mode rebuilds when template files change)
 *
 * Output is gitignored.
 */

import { mkdirSync, readFileSync, readdirSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath, pathToFileURL } from "node:url";

const SCRIPT_DIR = dirname(fileURLToPath(import.meta.url));
const PKG_DIR = dirname(SCRIPT_DIR);

export const TEMPLATES_DIR = join(PKG_DIR, "src", "templates");
export const OUTPUT_PATH = join(PKG_DIR, "src", "templates.generated.ts");

/** Absolute paths of every template file currently on disk. */
export function listTemplateFiles() {
  return readdirSync(TEMPLATES_DIR)
    .filter((name) => name.endsWith(".ts"))
    .sort()
    .map((name) => join(TEMPLATES_DIR, name));
}

function camelCase(name) {
  return name.replace(/-([a-z])/g, (_, ch) => ch.toUpperCase());
}

function quoteAsTsTemplateLiteral(source) {
  // Use a tagged template-literal-style string with backticks. Escape
  // backslashes, backticks, and `${` to keep the literal opaque.
  return source.replace(/\\/g, "\\\\").replace(/`/g, "\\`").replace(/\$\{/g, "\\${");
}

/**
 * Read every template file and write `src/templates.generated.ts`. Pure I/O;
 * idempotent. Returns the list of source paths read so callers can register
 * them as watch dependencies.
 */
export function generateTemplates() {
  const files = listTemplateFiles();

  const exportedConsts = files
    .map((path) => {
      const baseName = path.split("/").pop().replace(/\.ts$/, "");
      const constName = camelCase(baseName);
      const body = readFileSync(path, "utf8");
      return { baseName, constName, body };
    })
    .map(
      ({ baseName, constName, body }) =>
        `// Inlined from src/templates/${baseName}.ts\nexport const ${constName} = \`${quoteAsTsTemplateLiteral(body)}\`;\n`,
    )
    .join("\n");

  const lookupEntries = files
    .map((path) => {
      const baseName = path.split("/").pop().replace(/\.ts$/, "");
      const constName = camelCase(baseName);
      return `  ${JSON.stringify(baseName)}: ${constName},`;
    })
    .join("\n");

  const out = `// AUTO-GENERATED by scripts/generate-templates.mjs — do not edit by hand.
// Re-run via \`node scripts/generate-templates.mjs\`, or just run the tests /
// build, which trigger it automatically. Source of truth lives in
// \`src/templates/*.ts\`.

${exportedConsts}
export const TEMPLATES = {
${lookupEntries}
} as const;

export type TemplateName = keyof typeof TEMPLATES;
`;

  mkdirSync(dirname(OUTPUT_PATH), { recursive: true });
  writeFileSync(OUTPUT_PATH, out, "utf8");
  return files;
}

// CLI entry: only run gen when invoked directly (`node generate-templates.mjs`).
// Importers (vitest globalSetup, the tsdown plugin in vite.config.ts) call
// `generateTemplates()` themselves so the side effect is explicit.
if (import.meta.url === pathToFileURL(process.argv[1]).href) {
  generateTemplates();
}

// Vitest globalSetup contract: default-export a setup function. Vitest calls
// it once before any test module is imported, so `templates.generated.ts`
// exists by the time tests run.
export default function vitestGlobalSetup() {
  generateTemplates();
}
